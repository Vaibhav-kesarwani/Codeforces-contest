void run_case() {
    int N;
    int64_t X, Y;
    cin >> N >> X >> Y;
    vector<int64_t> A(N);

    for (auto &a : A)
        cin >> a;

    sort(A.begin(), A.end());
    int64_t A_sum = accumulate(A.begin(), A.end(), INT64_C(0));
    int64_t PX = A_sum - Y, PY = A_sum - X;
    int64_t ans = 0;

    for (int i = 0, x = N, y = N; i < N; i++) {
        while (x > 0 && A[i] + A[x - 1] >= PX)
            x--;

        while (y > 0 && A[i] + A[y - 1] > PY)
            y--;

        ans += max(y, i + 1) - max(x, i + 1);
    }

    cout << ans << '\n';
}

int main() {
    ios::sync_with_stdio(false);
#ifndef NEAL_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}
